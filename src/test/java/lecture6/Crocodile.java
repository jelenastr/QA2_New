package lecture6;

public class Crocodile {


    public int getCrocodileWeight() {
        return crocodileWeight;
    }

    public void setCrocodileWeight(int crocodileWeight) {
        this.crocodileWeight = crocodileWeight;
    }

    public String getCrocodileColor() {
        return crocodileColor;
    }

    public void setCrocodileColor(String crocodileColor) {
        this.crocodileColor = crocodileColor;
    }

    public int getCrocodileLength() {
        return crocodileLength;
    }

    public void setCrocodileLength(int crocodileLength) {
        this.crocodileLength = crocodileLength;
    }

    public String getCrocodileName() {
        return crocodileName;
    }

    public void setCrocodileName(String crocodileName) {
        this.crocodileName = crocodileName;
    }

    public Boolean getCrocodileHungry() {
        return crocodileHungry;
    }

    public void setCrocodileHungry(Boolean crocodileHungry) {
        this.crocodileHungry = crocodileHungry;
    }

    private int crocodileWeight;
    private String crocodileColor;
    private int crocodileLength;
    private String crocodileName;
    private Boolean crocodileHungry;

}
